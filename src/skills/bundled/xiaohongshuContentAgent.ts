export function registerXiaohongshuContentAgentSkill(): void {
  if (process.env.USER_TYPE !== 'ant') {
    return
  }

  registerBundledSkill({
    name: 'xiaohongshu-content-agent',
    description:
      '专业小红书内容创作代理，将复杂内容拆解为小红书风格的系列信息图。指定内容主题和要求作为参数。',
    argumentHint: '[content_topic] [requirements]',
    userInvocable: true,
    allowedTools: ['AskUserQuestion', 'Agent'],
    async getPromptForCommand(args) {
      if (!args) {
        return [
          {
            type: 'text',
            text: '请指定小红书内容主题和要求。例如：\n\n/xiaohongshu-content-agent productivity-tips "创建关于高效工作技巧的小红书内容，6张图，清新简约风格"\n\n/xiaohongshu-content-agent skincare-routine "制作护肤日常教程，8张图，温馨可爱风格"'
          }
        ]
      }

      const text = `你是一位专业的小红书视觉内容策划师。${XIAOHONGSHU_AGENT_PROMPT}\n\nUSER REQUEST: ${args}`

      return [
        {
          type: 'text',
          text: text
        }
      ]
    },
  })
}