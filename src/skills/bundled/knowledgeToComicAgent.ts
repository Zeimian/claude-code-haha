export function registerKnowledgeToComicAgentSkill(): void {
  if (process.env.USER_TYPE !== 'ant') {
    return
  }

  registerBundledSkill({
    name: 'knowledge-to-comic-agent',
    description:
      '将知识内容转换为漫画的专业代理。参考Logicomix叙事风格，专门处理科学探索历程、跨时代发展和复杂概念可视化。',
    argumentHint: '[knowledge_content] [topic_requirements]',
    userInvocable: true,
    allowedTools: ['AskUserQuestion'],
    async getPromptForCommand(args) {
      if (!args) {
        return [
          {
            type: 'text',
            text: '请提供需要转换为漫画的知识内容，以及特定的主题要求。例如：\n\n/knowledge-to-comic-agent "量子物理发展史" "科学探索主题，突出重要发现时刻"\n\n/knowledge-to-comic-agent "科学家传记" "人生轨迹，突出关键抉择"'
          }
        ]
      }

      const text = `你是专业的知识漫画创作专家。${KNOWLEDGE_TO_COMIC_AGENT_PROMPT}\n\nUSER REQUEST: ${args}`

      return [
        {
          type: 'text',
          text: text
        }
      ]
    },
  })
}